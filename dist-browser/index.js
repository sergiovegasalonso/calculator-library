/**
 * Adds two numbers.
 * @param a First number.
 * @param b Second number.
 * @returns Result.
 */
export var add = function (a, b) {
  return a + b
}
/**
 * Subtracts two numbers.
 * @param a First number.
 * @param b Second number.
 * @returns Result.
 */
export var subtract = function (a, b) {
  return a - b
}
/**
 * Multiplies two numbers.
 * @param a First number.
 * @param b Second number.
 * @returns Result.
 */
export var multiply = function (a, b) {
  return a * b
}
